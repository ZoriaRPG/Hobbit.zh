"use strict";

var colors = ["#000000", // 0: black
              "#0000FF", // 1: blue
              "#FF0000", // 2: red
              "#FF00FF", // 3: magenta
              "#00FF00", // 4: green
              "#00FFFF", // 5: cyan
              "#FFFF00", // 6: yellow
              "#FFFFFF"  // 7: white
             ];

var bits = [0];
var ink = [0];
var paper = [0];

function run(n) {
    var canvas = document.getElementById("gfx");
    var ctx = canvas.getContext("2d");
    clear(7, 0);
    var border = draw(gfxdata[n], ctx);
    canvas.style.borderColor = colors[border];
    refresh(ctx);
}

function clear(bg, fg) {
    for (var i = 0; i < 128*256; ++i) { bits[i] = 0; }
    for (var i = 0; i < 16*32; ++i) { ink[i] = fg; paper[i] = bg; }
}

function refresh(ctx) {
    for (var y = 0; y < 128; ++y) {
        for (var x = 0; x < 256; ++x) {
            var ax = x >> 3;
            var ay = y >> 3;
            var c;
            if (bits[y*256+x] == 0) {
                c = paper[ay*32+ax];
            } else {
                c = ink[ay*32+ax];
            }
            ctx.fillStyle = colors[c];
            ctx.fillRect(x,y,1,1);
        }
    }
}

// graphics opcodes:
// 0x08 X Y: move to X Y
// 0x80-0xFF N: draw line segment
// 0x40-0x7F X Y: fill area
// 0x20-0x3F H L N+: paint background
// 00: stop

function draw(data, ctx) {
    var pc = 0;
    // the first two bytes of graphics data are the border colour
    // and initial background+foreground colours
    var border=data[pc++];
    var bg = (data[pc] >> 3) & 7;
    var fg = data[pc++] & 7;
    clear(bg, fg);

    var op = 0;
    var x = 0;
    var y = 0;
    var c = 0;

    while (op = data[pc++], op != 0) {
       if (op == 8) {
           x = data[pc++];
           y = 127 - data[pc++];
           drawpixel(x, y, c);
       } else if (op > 0x7f) {
           var v = drawline(x, y, c,
                            (op & 0x07),
                            (data[pc] & 0x3f),
                            (((op & 0x78) >> 1) + ((data[pc] & 0xc0) >> 6))
                           );
           x = v[0];
           y = v[1];
           ++pc;
       } else if (op > 0x3f) {
           var fx = data[pc++];
           var fy = 127 - data[pc++];
           areafill(fx, fy, op & 0x07);
       } else if (op > 0x1f) {
           var H = data[pc++];
           var L = data[pc++];
           var a = (H*256+L)-0x5800;
           for (var d; d = data[pc++], d != 0xff;) {
               a = paintbg(a, op & 0x07, d & 0x03, (d & 0xfc) >> 2);
           }
       }
    }
    return border;
}

function checkpixel(x, y) {
    return (bits[y*256+x] == 1);
}

function drawpixel(x, y, c) {
    bits[y*256+x] = 1;
    var ax = x >> 3;
    var ay = y >> 3;
    if (paper[ay*32+ax] == c) {
        ink[ay*32+ax] = c^7;
    } else {
        ink[ay*32+ax] = c;
    }
}

function drawline(x, y, c, d, n, m) {
    var m0 = m;
    if (d & 1) {
        do {
            drawpixel(x, y, c)
            if (d & 2) {
                if (y < 127) ++y; else return [x, y];
            } else {
                if (y > 0) --y; else return [x, y];
            }
            if (m-- <= 0) {
                m = m0;
                if (d & 4) {
                    if (x > 0) --x; else return [x, y];
                } else {
                    if (x < 255) ++x; else return [x, y];
                }
            }
        } while (n-- > 0)
    } else {
        do {
            drawpixel(x, y, c)
            if (d & 4) {
                if (x > 0) --x; else return [x, y];
            } else {
                if (x < 255) ++x; else return [x, y];
            }
            if (m-- <= 0) {
                m = m0;
                if (d & 2) {                    // move down
                    if (y < 127) ++y; else return [x, y];
                } else {
                    if (y > 0) --y; else return [x, y];
                }
            }
        } while (n-- > 0)
    }
    return [x, y];
}

function paintbg(a, c, d, n) {
    do {
        if (ink[a] == c) ink[a] = c^7;
        paper[a] = c;
        switch (d) {
        case 0:
            if (a > 31) a -= 32;
            break;
        case 1:
            if (a < 511) ++a;
            break;
        case 2:
            if (a < 480) a += 32;
            break;
        case 3:
            if (a > 0) --a;
            break;
        }
    } while (n-- > 0)
    return a;
}

function areafill(x, y, c) {
    var stx = [x];
    var sty = [y];
    var sp = 1;
    while (sp > 0) {
        var ia = 0;
        var ib = 0;
        --sp;
        x = stx[sp];
        y = sty[sp];
        while (true) {
            if (checkpixel(x, y)) {
                drawpixel(x, y, c);
                ++x;
                break;
            } else {
                if (x > 0) --x; else break;
            }
        }
        do {
            if (y > 0 && !checkpixel(x, y-1)) {
                if (!ia) {
                    stx[sp] = x;
                    sty[sp] = y-1;
                    ++sp;
                    ia = 1
                };
            } else {
                ia = 0;
            }
            if (y < 127 && !checkpixel(x, y+1)) {
                if (!ib) {
                    stx[sp] = x;
                    sty[sp] = y+1;
                    ++sp;
                    ib = 1
                };
            } else {
                ib = 0;
            }
            drawpixel(x, y, c);
        } while (x < 255 && !checkpixel(++x, y))
        drawpixel(x, y, c);
    }
}

